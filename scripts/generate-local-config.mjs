#!/usr/bin/env node
/**
 * Reads .env (and optionally deploy/api-url-and-key.txt) and writes config.local.js
 * for localhost overrides. Does not inject API keys into the frontend bundle.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const envPath = path.join(repoRoot, '.env');
const legacyPath = path.join(repoRoot, 'deploy', 'api-url-and-key.txt');
const outPath = path.join(repoRoot, 'config.local.js');

function parseEnvFile(content) {
  const out = {};
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq < 1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

function readFileIfExists(filePath) {
  if (!fs.existsSync(filePath)) return {};
  return parseEnvFile(fs.readFileSync(filePath, 'utf8'));
}

function pick(values, ...keys) {
  for (const key of keys) {
    const v = values[key];
    if (typeof v === 'string' && v.trim()) return v.trim();
  }
  return '';
}

const merged = {
  ...readFileIfExists(legacyPath),
  ...readFileIfExists(envPath),
};

const apiBaseUrl = pick(merged, 'WQT_API_BASE_URL', 'API_BASE_URL');
const cognitoDomain = pick(merged, 'WQT_COGNITO_DOMAIN', 'COGNITO_DOMAIN');
const cognitoClientId = pick(merged, 'WQT_COGNITO_CLIENT_ID', 'COGNITO_CLIENT_ID');
const cognitoScopes =
  pick(merged, 'WQT_COGNITO_SCOPES', 'COGNITO_SCOPES') || 'openid email profile';

if (!apiBaseUrl && !cognitoDomain && !cognitoClientId) {
  console.error(
    'No local config found. Copy .env.example to .env or deploy/api-url-and-key.example.txt to deploy/api-url-and-key.txt, then set at least WQT_API_BASE_URL.',
  );
  process.exit(1);
}

const legacyApiKey = pick(merged, 'API_KEY');
if (legacyApiKey) {
  console.warn(
    'Note: API_KEY in .env / api-url-and-key.txt is ignored for the browser (JWT-only). Remove it from those files when no longer needed for deploy scripts.',
  );
}

const body = `// Generated by scripts/generate-local-config.mjs — do not commit
window.WQT_LOCAL_CONFIG = {
  apiBaseUrl: ${JSON.stringify(apiBaseUrl)},
  cognitoDomain: ${JSON.stringify(cognitoDomain)},
  cognitoClientId: ${JSON.stringify(cognitoClientId)},
  cognitoScopes: ${JSON.stringify(cognitoScopes)},
};
`;

fs.writeFileSync(outPath, body, 'utf8');
console.log('Wrote', path.relative(repoRoot, outPath));
if (apiBaseUrl) {
  try {
    const origin = new URL(apiBaseUrl).origin;
    const defaultOrigin = 'https://gnewkvhgwd.execute-api.eu-west-1.amazonaws.com';
    if (origin !== defaultOrigin) {
      console.warn(
        `Custom API host ${origin}: add it to the Content-Security-Policy connect-src in index.html if requests are blocked.`,
      );
    }
  } catch {
    console.warn('WQT_API_BASE_URL is not a valid URL; fix .env before serving locally.');
  }
}
